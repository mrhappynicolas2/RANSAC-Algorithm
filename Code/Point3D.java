public class Point3D {

    private double x;
    private double y;
    private double z;

    public Point3D(double x, double y, double z) {
      this.x = x;
      this.y = y;
      this.z = z;
    }
    
    // gets x-coordinate
    public double getX() {
        return x;
    }
  
    // gets y-coordinate
    public double getY() {
        return y;
    }	
  
    // gets z-coordinate
    public double getZ() {
        return z;
    }
  
  }
  